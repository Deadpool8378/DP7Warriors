package com.programs;

public class Casting {
	public static void main(String[]args){
		
	

	char ch = 'a';
	char ch1 = (char) (ch - 32);
	System.out.println(ch1);
	
	char txt='G';
	char ch3=(char)(txt+32);
	System.out.println(ch3);
	
	char a='d';
	char ch4=(char)(a-5);
	System.out.println(ch4);

}
}
